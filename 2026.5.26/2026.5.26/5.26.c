/*复数的整型提升，例如
* char c1 =-1;
* -1的原码1000 0000 0000 0000 0000 0000 0000 0001
* 反码：  1111 1111 1111 1111 1111 1111 1111 1110
* 补码：  1111 1111 1111 1111 1111 1111 1111 1111
* 变量c1的二进制为（补码）中只有8个比特位：1111 1111
* 因为char为有符号的char，所以整型提升的时候，高位补充符号位，即为1
* 提升之后结果为：1111 1111 1111 1111 1111 1111 1111 1111
* 
*  char c1 =1;
* 1的原码 0000 0000 0000 0000 0000 0000 0000 0001
* 反码：  0000 0000 0000 0000 0000 0000 0000 0001
* 补码：  0000 0000 0000 0000 0000 0000 0000 0001
* 变量c1的二进制为（补码）中只有8个比特位：0000 0001
* 因为char为有符号的char，所以整型提升的时候，高位补充符号位，即为0
* 提升之后结果为：0000 0000 0000 0000 0000 0000 0000 0001
* 无符号整型的提升，高位直接补0
* 
* char a=5;
* char b=126;
* char c=a+b;
* printf("%d",c)
* c=???
* 5的二进制序列0000 0000 0000 0000 0000 0000 0000 0101
* a里面实际放的是0000 0101
* 126的二进制序列0000 0000 0000 0000 0000 0000 0111 1110
* b里面实际放的是0111 1110
* a ,b达不到整型大小，发生整型提升
* a提升后0000 0000 0000 0000 0000 0000 0000 0101
* b提升后0000 0000 0000 0000 0000 0000 0111 1110
* a + b =0000 0000 0000 0000 0000 0000 1000 0011
* c=1000 0011,因为打印c的是整型，所以要整型提升
* c整型提升后		1111 1111 1111 1111 1111 1111 1000 0011 -补码
*					1111 1111 1111 1111 1111 1111 1000 0010 -反码
* 取反，符号位不变  1000 0000 0000 0000 0000 0000 0111 1101 -原码
* 所以打印结果为-125
*/


